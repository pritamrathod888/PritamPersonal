package com.java;

public class Operator_Relational {                              // relational operator ch answer true false madhe yeto

	public static void main(String[] args) {

		int a=10,b=20;
		System.out.println(a==b);    //10==20
		System.out.println(a!=b);    //10!=20
		System.out.println(a>b);     //10>20
		System.out.println(a<b);     //10<20
		System.out.println(a<=b);    //10<=20
		System.out.println(a>=b);    //10>=20
		

	}

}
